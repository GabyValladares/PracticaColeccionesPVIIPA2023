     private void actualizarTabla() {
        // Limpiar la tabla
        DefaultTableModel modeloJovenes = (DefaultTableModel) tbJovenes.getModel();
        DefaultTableModel modeloMayores = (DefaultTableModel) tbMayores.getModel();
        modeloJovenes.setRowCount(0);
        modeloMayores.setRowCount(0);

        for (Jugador jugador : listaJugador) {
            if (jugador.getEdad() < 23) {
                modeloJovenes.addRow(new Object[] { jugador.getApellido(), jugador.getEdad(), jugador.getPuesto(), jugador.getNacionalidad(), jugador.getGoles() });
            } else {
                modeloMayores.addRow(new Object[] { jugador.getApellido(), jugador.getEdad(), jugador.getPuesto(), jugador.getNacionalidad(), jugador.getGoles() });
            }
           
        if (jugador.getEdad() < 23) {
            if (jugador.getGoles() > golesGoleadorJovenes) {
                golesGoleadorJovenes = jugador.getGoles();
                nombreGoleadorJovenes = jugador.getApellido();
                lblGol1.setText("Goleador Joven: " + nombreGoleadorJovenes + " - Goles: " + golesGoleadorJovenes);
            }
        } else {
            if (jugador.getGoles() > golesGoleadorMayores) {
                golesGoleadorMayores = jugador.getGoles();
                nombreGoleadorMayores = jugador.getApellido();
                lblGol2.setText("Goleador Mayor: " + nombreGoleadorMayores + " - Goles: " + golesGoleadorMayores);
            }
        }
        }
        
    }
    private void btnRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegActionPerformed
        // TODO add your handling code here:
        try {
         String apellido = txtApellido.getText();
            int edad = Integer.parseInt(txtEdad.getText());
            String nacion = txtNacion.getText();
            int gol = Integer.parseInt(txtGol.getText());
            String puesto = Combo2.getSelectedItem().toString();
            
            Jugador jugador = new Jugador(apellido, edad, puesto, nacion, gol);
            listaJugador.add(jugador);
            
            
            actualizarTabla();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al ingresar los datos");
        }
        
    }//GEN-LAST:event_btnRegActionPerformed


    private void imprimirTablas() {

        for (Iterator<Integer> iterator = cola1.iterator(); iterator.hasNext();) {
            Integer next1 = iterator.next();

            dfm1.addRow(new Integer[]{next1.intValue()});

        }
        for (Iterator<Integer> iterator = cola2.iterator(); iterator.hasNext();) {
            Integer next = iterator.next();

            dfm2.addRow(new Integer[]{next.intValue()});

        }
    }


    private void txtPalabraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPalabraActionPerformed
        if (!txtPalabra.getText().isEmpty()) {
            if (contadorPalabras < 10) {
                String palabraActual = txtPalabra.getText();
                palabras[contadorPalabras] = palabraActual;
                contadorPalabras++;
                txtPalabra.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ya ingresó 10 números");
                for (int i = 0; i < palabras.length; i++) {
                    System.out.println(palabras[i]);
                }
                tbla.addRow(new String[]{palabras[0],palabras[1],palabras[2],palabras[3],palabras[4],palabras[5],palabras[6],palabras[7],palabras[8],palabras[9]});
            }
        }
    }//GEN-LAST:event_txtPalabraActionPerformed



    private void txtTextoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTextoActionPerformed
        try {
         int numero = Integer.parseInt(txtTexto.getText());
            txtTexto.setText("");

            if (numero != -99) {
                numerosEnteros.add(numero);
                
        int cantidadNumeros = numerosEnteros.size();
        int suma = 0;

        for (int num : numerosEnteros) {
            suma += num;
        }
        
        tableN.addRow(new Object[]{numero});
        double promedio = (double) suma / cantidadNumeros;
        JOptionPane.showMessageDialog(this, "Suma de los valores: " + suma);
        JOptionPane.showMessageDialog(this, "Promedio de los valores: " + promedio);
      
        int contadorMayoresQuePromedio = 0;

        for (int num : numerosEnteros) {
            if (num > promedio) {
                contadorMayoresQuePromedio++;
                tableN.setValueAt("MP", contadorMayoresQuePromedio - 1, 1);
            }
     }
 }
            
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Ingrese solo números");
        }

    }//GEN-LAST:event_txtTextoActionPerformed



    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
             // Obtener el número ingresado por el usuario
        int numero = Integer.parseInt(txtElementos.getText());
        
        // Agregar el número a la lista
        listaNumeros.add(numero);
        
        // Actualizar el modelo de la tabla 1 con los números ingresados
        DefaultTableModel modelo1 = (DefaultTableModel) tbLista1.getModel();
        modelo1.addRow(new Object[]{numero});
        
        // Encontrar el número mayor y menor
        int numeroMayor = Collections.max(listaNumeros);
        int numeroMenor = Collections.min(listaNumeros);
        
        // Mostrar el número mayor y menor en etiquetas o imprimirlos
        lblMayor.setText("Número Mayor: " + numeroMayor);
        lblMenor.setText("Número Menor: " + numeroMenor);
        
        // Actualizar el modelo de la tabla 2 con los números en orden inverso
        DefaultTableModel modelo2 = (DefaultTableModel) tbLista2.getModel();
        modelo2.setRowCount(0); // Limpiar la tabla antes de agregar los datos
        
        for (int i = listaNumeros.size() - 1; i >= 0; i--) {
            modelo2.addRow(new Object[]{listaNumeros.get(i)});
        }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No ingreso datos");
        }
       
    }//GEN-LAST:event_btnGuardarActionPerformed



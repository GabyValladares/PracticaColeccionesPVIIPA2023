        // TODO add your handling code here:
        temp1.addAll(cola1);
        temp2.addAll(cola2);

        while (!temp1.isEmpty() && !temp2.isEmpty()) {
            int suma = temp1.poll() + temp2.poll();
            cola3.add(suma);

        }
        for (Iterator<Integer> iterator = cola3.iterator(); iterator.hasNext();) {
            Integer next = iterator.next();

            dfm3.addRow(new Integer[]{next.intValue()});

        }



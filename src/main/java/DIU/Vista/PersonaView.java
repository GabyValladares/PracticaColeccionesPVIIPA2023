    public void setModelo() {
        String[] cabecera = {"Nro.", "Nombres", "Apellidos", "Cédula", "Usuario", "Clave"};
        modelo.setColumnIdentifiers(cabecera);
        tblPersonas.setModel(modelo);
    }
    
    private void setDatos() {
        // ESTRUCTURA INFORMACIÓN TABLA
        Object[] filas = new Object[modelo.getColumnCount()];
        int contador = 1;
        for (PersonaModel datos : ListaPersonaModelo) {
            filas[0] = contador;
            filas[1] = datos.getNombres();
            filas[2] = datos.getApellidos();
            filas[3] = datos.getCedula();
            filas[4] = datos.getUsuario();
            filas[5] = datos.getClave();
            modelo.addRow(filas);
            
            contador++;
        }
        tblPersonas.setModel(modelo);
    }
    

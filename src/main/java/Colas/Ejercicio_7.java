    private void btnAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAActionPerformed
        // TODO add your handling code here:
         try {
            int num1 = Integer.parseInt(txtC1.getText());
            int num2 = Integer.parseInt(txtC2.getText());
            
            cola1.offer(num1);
            cola2.offer(num2);
            
            modeloTabla1.setRowCount(0);
            modeloTabla2.setRowCount(0);
            
            for (Integer num : cola1) {
                modeloTabla1.addRow(new Object[]{num});
            }
            for (Integer num : cola2) {
                modeloTabla2.addRow(new Object[]{num});
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese n√∫meros en las dos casillas");
        }                                          

    }//GEN-LAST:event_btnAActionPerformed



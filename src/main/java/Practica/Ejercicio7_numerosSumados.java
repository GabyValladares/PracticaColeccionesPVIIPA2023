    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
      
        Queue<Integer> cola1 = new LinkedList<>();
        Queue<Integer> cola2 = new LinkedList<>();
        Queue<Integer> total = new LinkedList<>();
        
        int cantidad1 = Integer.parseInt(txtCantidad1.getText());
        txtCantidad1.setText("");
        int cantidad2 = Integer.parseInt(txtCantidad2.getText());
        txtCantidad2.setText("");
        
        cola1.offer(cantidad1);
        cola2.offer(cantidad2);
                
        mostarTablas(cola1, cola2, total, tbControl);
        
        int sumaTotal = 0;
        for (int num : total) {
            sumaTotal += num;
    }
           
           JOptionPane.showMessageDialog(this, "El total de las flores vendidas en los dos meses es: "+ sumaTotal);
   
    }//GEN-LAST:event_btnTotalActionPerformed

     private void mostarTablas(Queue<Integer> cola1, Queue<Integer> cola2, Queue<Integer> total, javax.swing.JTable tbControl){
       DefaultTableModel modeloResultado = (DefaultTableModel) tbControl.getModel();
       String cabecera [] = {"Enero","Febrero"};
        modeloResultado.setColumnIdentifiers(cabecera);
        
        int fila = 0;

        if (!cola1.isEmpty() && !cola2.isEmpty()) {
            int cantidad1 = cola1.poll();
            int cantidad2 = cola2.poll();
            int suma = cantidad1 + cantidad2;
            total.offer(suma);
            modeloResultado.addRow(new Object[]{cantidad1, cantidad2});
        }
    }

    private void BttnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BttnVerificarActionPerformed
        // TODO add your handling code here:
        
       String listaPalabras = txtPalabra.getText().trim();
    
    // Obtener la palabra ingresada en TxtComprobarPalabra
    String palabraComprobar = TxtComprobarPalabra.getText().trim();

    // Verificar si la lista de palabras está vacía
    if (listaPalabras.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa una lista de palabras en txtPalabra.");
        return; 
    }

    // Verificar si la palabra a comprobar está vacía
    if (palabraComprobar.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa una palabra para verificar cuantas veces aparece.");
        return; 
    }

    String[] palabras = listaPalabras.split("\\s+");

    int contador = 0;

    for (String palabra : palabras) {
        if (palabra.equalsIgnoreCase(palabraComprobar)) {
            contador++;
        }
    }

    // Mostrar el resultado en TxtAreaVerificar
    TxtAreaVerificar.setText("La palabra '" + palabraComprobar + "' aparece " + contador + " veces en la lista:\n" + listaPalabras);
    }//GEN-LAST:event_BttnVerificarActionPerformed



    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String puesto = cmbOpcion.getSelectedItem().toString();
        String opcion = cmbOpcion.getSelectedItem().toString();

        Jugador jugador = new Jugador(txtApellidos.getText(), txtEdad.getText(), puesto + opcion, txtNacionalidad.getText(), txtGoles.getText());

        listaJugadores.add(jugador);

        dfm.addRow(new Object[]{
            jugador.getApellido(), jugador.getEdad(), jugador.getPuesto(), jugador.getNacionalidad(), jugador.getGoles()
        });

        if (Integer.parseInt(jugador.getEdad()) < 23) {
            listaJugadoresJuveniles.add(jugador);

        } else if (Integer.parseInt(jugador.getEdad()) >= 23) {
            listaJugadoresSeniors.add(jugador);
        }


    }//GEN-LAST:event_jButton1ActionPerformed


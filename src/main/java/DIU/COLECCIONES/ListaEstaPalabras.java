    private void BtContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtContarActionPerformed
        // TODO add your handling code here:
        int cont = 0;
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        int filaSeleccionada = jTable1.getSelectedRow();

        if (filaSeleccionada != -1) {
            Object valorCelda = modelo.getValueAt(filaSeleccionada, 0);
            String palabraSeleccionada = valorCelda.toString();

            for (String Palabra : ListaPalabras) {
                if (Palabra.equalsIgnoreCase(palabraSeleccionada)) {
                    cont++;
                }
            }
            TablaD.setValueAt(cont,jTable1.getSelectedRow(),1);
        }
    }//GEN-LAST:event_BtContarActionPerformed

    private void btAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarActionPerformed
        // TODO add your handling code here:
        String nuevaP = TxtDatos.getText();

        if(!nuevaP.isEmpty()){
            ListaPalabras.add(nuevaP);
            TablaD.addRow(new Object[]{nuevaP});
            lbMensaje.setForeground(Color.GREEN);
            lbMensaje.setBackground(Color.WHITE);
            lbMensaje.setFont(new Font("Segoe UI Black", Font.ITALIC, 18));
            lbMensaje.setText("Palabra agregada correctamente");
            TxtDatos.setText("");
        }else{
            lbMensaje.setText("Texto vacio, no se agrego la palabra");
        }
    }//GEN-LAST:event_btAgregarActionPerformed

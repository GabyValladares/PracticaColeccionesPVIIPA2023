    public void SetDatos(){
     Object[] informacion=new Object[modelo.getColumnCount()];
     int cont=1;
        modelo.setRowCount(0);
        for (Persona datos : ListaPersona) {
            informacion[0]=cont;
            informacion[1]=datos.getNombres();
            informacion[2]=datos.getApellidos();
            informacion[3]=datos.getCedula();
            informacion[4]=datos.getUsuario();
            informacion[5]=datos.getClave();
            
            cont++;
            modelo.addRow(informacion);
        }
        TablaDatos.setModel(modelo);
    }


    private void btnComprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprobarActionPerformed
  try {
    int numero = Integer.parseInt(txtTamaño.getText());
    String oracion = txtPalabras.getText();
    String[] palabras = oracion.split("\\s+");

    List<String> listaCombinada = new ArrayList<>();
    for (int i = 0; i < numero; i++) {
        // Agregar la palabra en la posición par
        listaCombinada.add(palabras[i % palabras.length]);
    }

    // Imprimir la lista combinada en el JTextArea (reemplaza "txtResultado" con el nombre real de tu JTextArea)
    StringBuilder resultado = new StringBuilder("El resultado es: ");
    for (String palabra : listaCombinada) {
        resultado.append(palabra).append(" ");
    }
    txtResultado.setText(resultado.toString());
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Ingrese un número válido para la dimensión", "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_btnComprobarActionPerformed


    private void btnAgregarNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarNActionPerformed
        DefaultTableModel tabla = (DefaultTableModel) JTablaNMA.getModel();
        DefaultTableModel tabla2 = (DefaultTableModel) JTablaNLe.getModel();
        DefaultTableModel tabla3 = (DefaultTableModel) JTablaSumT.getModel();
        DefaultTableModel tabla4 = (DefaultTableModel) JTablaProm.getModel();

        try {
            int num = Integer.parseInt(txtNumero.getText());

            if (num == -99 && !listaNumeros.isEmpty()) {
                for (int numAc : listaNumeros) {
                    suma += numAc;
                }
                datos3[0] = String.valueOf(suma);
                tabla3.addRow(datos3);
                promedio = suma / listaNumeros.size();
                datos4[0] = String.valueOf(promedio);
                tabla4.addRow(datos4);

                for (int numAc : listaNumeros) {
                    datos[0] = numAc;
                    tabla2.addRow(datos);

                    if (numAc > promedio) {
                        datos2[0] = numAc;
                        tabla.addRow(datos2);
                    }
                }

                listaNumeros.clear();
                suma = 0;
                promedio = 0;
            } else {
                listaNumeros.add(num);
            }
        } catch (NumberFormatException e) {
            System.out.println("Por favor, ingrese un número válido.");
        }
        txtNumero.setText("");
    }//GEN-LAST:event_btnAgregarNActionPerformed


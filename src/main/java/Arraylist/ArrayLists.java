    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int tamanio = Integer.parseInt(TxtTamaño.getText());

    // Obtener la oración ingresada
    String palabra = Txtpalabra.getText();

    // Dividir la oración en palabras
    String[] palabras = palabra.split(" ");

    // Crear un ArrayList para almacenar las palabras
    ArrayList<String> lista = new ArrayList<>();

    // Asignar palabras a posiciones pares e impares
    for (int i = 0; i < tamanio; i++) {
        lista.add(palabras[i % palabras.length]);
    }

    // Imprimir lista en el JTextArea
    StringBuilder mensaje = new StringBuilder("Lista:\n");
    for (int i = 0; i < lista.size(); i++) {
        mensaje.append("Posición ").append(i).append(": ").append(lista.get(i)).append("\n");
    }

    // Mostrar el resultado en el JTextArea
    txtArea.setText(mensaje.toString());

    }//GEN-LAST:event_jButton1ActionPerformed


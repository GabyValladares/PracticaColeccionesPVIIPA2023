    public void setdatos(){
    Object[]info= new Object[model.getColumnCount()];
    int cot = 1;
    model.setRowCount(0);
        for (perosnamodel p : lista){
        info[0]= cot ;
        info[1]= p.getNombres();
        info[2]= p.getApellidos();
        info[3]= p.getCedula();
        info[4]= p.getUsuario();
        info[5]= p.getClave();
        cot++;
        
        model.addRow(info);
        }
        tbl_tabla.setModel(model);
    }
    
    
    
    
    


    public void agregarNumeros(int numero) {
        while (numero != (-99)) {
            numeros.add(numero);
            
            break;
        }
    }

    public int suma() {
        int suma = 0;
        for (int numero : numeros) {
            suma += numero;
        }
        return suma;
    }

    public double promedio() {
        double promedio = 0;
        int sumar = this.suma();
        promedio = sumar / numeros.size();
        return promedio;
    }

    public int mayorPromedio() {
        int cantidad = 0;
        double promedios = this.promedio();
        for (int numero : numeros) {
            if (numero < promedios) {
                cantidad++;
            }
        }
        return cantidad;
    }



     private int[] convertirCadenaAArray(String input) {
        String[] strArray = input.trim().split("\\s+");
        int[] intArray = new int[strArray.length];
        for (int i = 0; i < strArray.length; i++) {
            try {
                intArray[i] = Integer.parseInt(strArray[i]);
            } catch (NumberFormatException e) {
                // Manejar el caso en que un elemento no sea un número
                System.out.println("Ingrese solo números válidos.");
                return new int[0];
            }
        }
        return intArray;
    }

    // Método auxiliar para encontrar el valor máximo en un arreglo
    private int encontrarMaximo(int[] array) {
        int max = array[0];
        for (int num : array) {
            if (num > max) {
                max = num;
            }
        }
        return max;
    }

    // Método auxiliar para encontrar el valor mínimo en un arreglo
    private int encontrarMinimo(int[] array) {
        int min = array[0];
        for (int num : array) {
            if (num < min) {
                min = num;
            }
        }
        return min;
    }

     private void mostrarResultadosEnTabla(int[] lista1, int[] lista2) {
        DefaultTableModel model = (DefaultTableModel) TblTabla.getModel();
        model.setRowCount(0); // Limpiar la tabla antes de agregar nuevos datos

        // Agregar fila con resultados de Lista1
        model.addRow(new Object[]{"Lista1", Arrays.toString(lista1)});

        // Agregar fila con resultados de Lista2 invertida
        model.addRow(new Object[]{"Lista2 invertida", Arrays.toString(lista2)});
    }

    // Método auxiliar para invertir el orden de los números en un arreglo
    private int[] invertirArray(int[] array) {
        int[] arrayInvertido = new int[array.length];
        for (int i = 0; i < array.length; i++) {
            arrayInvertido[i] = array[array.length - 1 - i];
        }
        return arrayInvertido;
    
}
    


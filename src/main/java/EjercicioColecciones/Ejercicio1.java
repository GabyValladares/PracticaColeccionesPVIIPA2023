    private void btnVerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerActionPerformed
     DefaultTableModel modelo =(DefaultTableModel) Jtabla.getModel();
     String cadena = txtnumeros.getText();
     String[] numerosString = cadena.split(",");
        Object[] filaDatos = new Object[4];
     
      if (numerosString.length != 10){
      JOptionPane.showMessageDialog(this, "porfavor ingrese 10 numeros");
      return;  
      }
      int[] Lista1 = new int [10];
      int[] Lista2 = new int [10];
        for (int i = 0; i < 10; i++) {
             Lista1[i] = Integer.parseInt(numerosString[i]);
             Lista2[9 - i] = Integer.parseInt(numerosString[i]);
        }
        int mayor = Lista1[0];
        int menor = Lista1[0];
        for (int i = 0; i < 10; i++) {
            if (Lista1[i]>mayor){
                mayor = Lista1[i];
            }
            if (Lista1[i]< menor){
                menor = Lista1[i];
            }
        }
        for (int i = 0; i < 10; i++) {
            filaDatos[0] = Lista1[i];
            filaDatos[1] = Lista2[i];
            filaDatos[2] = mayor;
            filaDatos[3] = menor;
                modelo.addRow(filaDatos.clone());
        }
       
        

    }//GEN-LAST:event_btnVerActionPerformed



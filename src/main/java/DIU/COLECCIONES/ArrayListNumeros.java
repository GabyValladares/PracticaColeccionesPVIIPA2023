    private void bttAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttAgregarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) JTablaNroMayores.getModel();
        DefaultTableModel modelo2 = (DefaultTableModel) JTablaNumeros.getModel();
        DefaultTableModel modelo3 = (DefaultTableModel) JTablaST.getModel();
        DefaultTableModel modelo4 = (DefaultTableModel) JTablaPromedio.getModel();

        try {
            int num = Integer.parseInt(txtNumero.getText());

            if (num == -99 && !listaNumeros.isEmpty()) {
                for (int numAc : listaNumeros) {
                    sum += numAc;
                }
                filaDatos3[0] = String.valueOf(sum);
                modelo3.addRow(filaDatos3);
                prom = sum / listaNumeros.size();
                filaDatos4[0] = String.valueOf(prom);
                modelo4.addRow(filaDatos4);
    
                for (int numAc : listaNumeros) {
                    filaDatos[0] = numAc;
                    modelo2.addRow(filaDatos);

                    if (numAc > prom) {
                        filaDatos2[0] = numAc;
                        modelo.addRow(filaDatos2);
                    }
                }

                listaNumeros.clear();
                sum = 0;
                prom = 0;
            } else {
                listaNumeros.add(num);
            }
           txtNumero.setText("");
        } catch (NumberFormatException e) {
            System.out.println("Por favor, ingrese un número válido.");
        
        txtNumero.setText("");
       }
    }//GEN-LAST:event_bttAgregarActionPerformed
    


       private void procesarDatos() {
        String input = txtLista.getText();
        ArrayList<Integer> numeros = new ArrayList<>();

        // Parsea la entrada y llena el ArrayList
        String[] partes = input.split(",");
        for (String parte : partes) {
            try {
                int numero = Integer.parseInt(parte.trim());
                if (numero == -99) {
                    break; 
                }
                numeros.add(numero);
            } catch (NumberFormatException ex) {
            }
        }

        // Calcula estadísticas
        int cantidad = numeros.size();
        int suma = 0;
        for (int numero : numeros) {
            suma += numero;
        }
        double promedio = cantidad > 0 ? (double) suma / cantidad : 0;
        tableModel.setRowCount(0);

        // Muestra una fila con las estadísticas en la tabla
        tableModel.addRow(new Object[]{
                cantidad,
                suma,
                promedio,
                contarMayoresQuePromedio(numeros, promedio)
        });

        txtLista.setText("");
    }

 

      private int contarMayoresQuePromedio(ArrayList<Integer> numeros, double promedio) {
        int contador = 0;
        for (int numero : numeros) {
            if (numero > promedio) {
                contador++;
            }
        }
        return contador;
    }

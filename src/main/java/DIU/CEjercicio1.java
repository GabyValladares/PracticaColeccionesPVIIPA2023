    LinkedList<Integer> cola1=new LinkedList();
    LinkedList<Integer> cola2=new LinkedList();
    LinkedList<Integer> resultado=new LinkedList();
    
    private void btnSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumarActionPerformed
        while(!cola1.isEmpty() && !cola2.isEmpty()){
            resultado.offer(cola1.poll() + cola2.poll());
        }
        String concat="";
        for (Integer valor : resultado) {
            concat=concat+" "+valor;
        }
        lblResultado.setText(concat);
    }//GEN-LAST:event_btnSumarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        int numero1;
        int numero2;
        if(txtCola1.getText().isEmpty()){
            numero1=0;
            numero2=Integer.parseInt(txtCola2.getText());
            cola1.offer(numero1);
            cola2.offer(numero2);
            txtCola1.setText("");
            txtCola2.setText("");
        }else if(txtCola2.getText().isEmpty()){
            numero2=0;
            numero1=Integer.parseInt(txtCola1.getText());
            cola1.offer(numero1);
            cola2.offer(numero2);
            txtCola1.setText("");
            txtCola2.setText("");
        }else{
            numero2=Integer.parseInt(txtCola2.getText());
            numero1=Integer.parseInt(txtCola1.getText());
            cola1.offer(numero1);
            cola2.offer(numero2);
            txtCola1.setText("");
            txtCola2.setText("");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed



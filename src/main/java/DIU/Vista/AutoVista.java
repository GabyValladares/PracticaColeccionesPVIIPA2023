    public void mostrarDatos(Auto auto){
        Object[] fila = new Object[10];

    fila[0] = modeloAUTO.getRowCount() + 1;
    fila[1] = auto.getPlaca(); 
    fila[2] = auto.getMarca();
    fila[3] = auto.getColor();
    fila[4] = auto.getTipo();
    fila[5] = auto.getValor();
    modeloAUTO.addRow(fila);
    }

    private void txtInvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtInvertirActionPerformed
        if (!txtPalabra.getText().isEmpty()) {
            String palabra = txtPalabra.getText();
            char letra;
            for (int i = 0; i < palabra.length(); i++) {
                letra=palabra.charAt(i);
                letras.push(letra);                
            }
            String concatInvertida="";
            while (!letras.isEmpty()) {                
                concatInvertida+=letras.pop();
            }
            txtInvertida.setText(concatInvertida);
        }
    }//GEN-LAST:event_txtInvertirActionPerformed



    private void limpiarTabla() {
        int filas = dfm.getRowCount();
        for (int i = 0; i < filas; i++) {
            dfm.removeRow(0);

        }
    }

    private void imprimirJuveniles() {
        limpiarTabla();

        for (Jugador jugador : listaJugadoresJuveniles) {

            dfm.addRow(new Object[]{
                jugador.getApellido(), jugador.getEdad(), jugador.getPuesto(), jugador.getNacionalidad(), jugador.getGoles()
            });

        }
    }

    private void imprimirSeniors() {
        limpiarTabla();

        for (Jugador jugador : listaJugadoresSeniors) {

            dfm.addRow(new Object[]{
                jugador.getApellido(), jugador.getEdad(), jugador.getPuesto(), jugador.getNacionalidad(), jugador.getGoles()
            });
        }
    }

    private void goleadorJuvenil() {
        Jugador jugadorMayorGoles = null;

        for (Jugador jugador : listaJugadoresJuveniles) {
            if (jugadorMayorGoles == null || Integer.parseInt(jugador.getGoles()) > Integer.parseInt(jugadorMayorGoles.getGoles())) {
                jugadorMayorGoles = jugador;
            }

        }

        String goleador1 = jugadorMayorGoles.getApellido() + jugadorMayorGoles.getGoles();

        lblGoleadorJuvenil.setText("Nombre: " + goleador1 + "Nro de goles" + jugadorMayorGoles.getGoles());
    }

    private void goleadorSenior() {
        Jugador jugadorMayorGoles = null;
        for (Jugador jugador : listaJugadoresSeniors) {
            if (jugadorMayorGoles == null || Integer.parseInt(jugador.getGoles()) > Integer.parseInt(jugadorMayorGoles.getGoles())) {
                jugadorMayorGoles = jugador;
            }

        }
        System.out.println("goleador senior" + jugadorMayorGoles.getApellido() + jugadorMayorGoles.getGoles());
        String goleador1 = jugadorMayorGoles.getApellido() + jugadorMayorGoles.getGoles();

        lblGoleadorSenior.setText("Nombre: " + goleador1 + "Nro de goles" + jugadorMayorGoles.getGoles());
    }

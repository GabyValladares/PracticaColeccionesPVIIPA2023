 // Método para contar cuántas veces aparece una palabra en la lista
    private int contarPalabra(String palabra) {
        int contador = 0;
        for (String p : listaPalabras) {
            if (p.equalsIgnoreCase(palabra)) {
                contador++;
            }
        }
        return contador;
    }
    private void btnLlenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLlenarActionPerformed
        // TODO add your handling code here:
        try {
           String palabra = txtPalabra.getText();
            modelo = (DefaultTableModel) this.tbLista.getModel();
            Object datos[] = new Object[] {palabra};
            modelo.addRow(datos);

            // Agregar la palabra a la lista de palabras
            listaPalabras.add(palabra);

            // Contar las veces que aparece la palabra en la lista
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "No a ingresado palabras");
        }
    }//GEN-LAST:event_btnLlenarActionPerformed

    private void btnContarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContarActionPerformed
        // TODO add your handling code here:
        String Cpalabra = txtCPalabra.getText();
        int contador = contarPalabra(Cpalabra);
            lblRes.setText("La palabra '" + Cpalabra + "' aparece " + contador + " veces.");
    }//GEN-LAST:event_btnContarActionPerformed



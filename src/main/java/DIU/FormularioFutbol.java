    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Crear una instancia de Jugador
        Jugador jugador = new Jugador();

        // Obtener los valores de los campos de texto y combos
        String apellidos = txtApellidos.getText();
        int edad = Integer.parseInt(txtEdad.getText());
        String puesto = cbxPuesto.getSelectedItem().toString();
        String posicion = cbxPosicion.getSelectedItem().toString();
        int goles = Integer.parseInt(txtGoles.getText());
        String nacionalidad = txtNacionalidad.getText();

        // Establecer los valores en el objeto Jugador
        jugador.setApellidos(apellidos);
        jugador.setEdad(edad);
        jugador.setPuesto(puesto);
        jugador.setPosicion(posicion);
        jugador.setGoles(goles);
        jugador.setNacionalidad(nacionalidad);

        // Asignar categoría al jugador
        if (edad < 23) {
            jugador.setCategoria("Juvenil");
        } else {
            jugador.setCategoria("Senior");
        }

        // Agregar el jugador a la lista
        listaJugadores.add(jugador);

        // Limpiar los campos después de guardar
        limpiarCampos();
        System.out.println("Jugador guardado:");
        System.out.println("Apellidos: " + jugador.getApellidos());
        System.out.println("Edad: " + jugador.getEdad());
        System.out.println("Puesto: " + jugador.getPuesto());
        System.out.println("Posición: " + jugador.getPosicion());
        System.out.println("Goles: " + jugador.getGoles());
        System.out.println("Nacionalidad: " + jugador.getNacionalidad());
        System.out.println("Categoría: " + jugador.getCategoria());
        System.out.println("Jugador guardado en la lista:");

    }//GEN-LAST:event_btnGuardarActionPerformed
// Método para limpiar los campos después de guardar

    private void limpiarCampos() {
    txtApellidos.setText("");
    txtEdad.setText("");
    cbxPuesto.setSelectedIndex(0);
    cbxPosicion.removeAllItems();
    txtGoles.setText("");
    txtNacionalidad.setText("");
}


        // TODO add your handling code here:
           String cadena = txtc1.getText();
        // Recorre cada car√°cter en la cadena y lo apila
        for (char c : cadena.toCharArray()) {
            pila.push(c);
        }
        // Construye una cadena inversa extrayendo caracteres de la pila
        String resultado = "";
        while (!pila.isEmpty()) {
            resultado += pila.pop();
        }
        txtc2.setText(resultado);

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Nombre = txt_nombre.getText();
        Apellido = txt_apellido.getText();
        Cedula = txt_cedula.getText();
        Usuario = txt_usuario.getText();
        Clave = txt_clave.getText();
        
        try (Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS)) {
            // Llamar al procedimiento almacenado
            try (CallableStatement statement = conn.prepareCall("{call sp_CrearPersona(?, ?, ?, ?, ?)}")) {
                // Establecer los par√°metros del procedimiento
                statement.setString(1, Nombre);
                statement.setString(2, Apellido);
                statement.setString(3, Cedula);
                statement.setString(4, Usuario);
                statement.setString(5, Clave);

                // Ejecutar el procedimiento almacenado
                statement.execute();
                System.out.println("Usuario guardado correctamente mediante procedimiento almacenado.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed



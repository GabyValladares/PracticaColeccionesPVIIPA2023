    public void setModelo() {
        String cabecera[] = {"nro", "CEDULA", "NOMBRES", "APELLIDOS", "ESTADO", "SERVICIO"};

        modelo.setColumnIdentifiers(cabecera);
        tblDatos.setModel(modelo);

    }

    public void llenarTabla() {
        int c1 = Integer.parseInt(txtCedula.getText());
        String nombres = txtNombres.getText();
        String apellidos = txtApellidos.getText();
        String opcion = cmbServicios.getSelectedItem().toString();

        PersonaSeguro p = new PersonaSeguro(c1, nombres, apellidos, "llegada", opcion);
        colaPS.add(p);

        switch (opcion) {
            case "consultas":
                colaConsultas.add(p);
                break;
            case "cancelaciones":
                colaCancelaciones.add(p);
                break;
            case "solicitud":
                colaSolicitudes.add(p);

                break;
            default:
                break;
        }

    }

    public void setDatos() {
        Object[] informacion = new Object[modelo.getColumnCount()];
        int contador = 1;
        modelo.setRowCount(0);
        for (PersonaSeguro persona : colaPS) {

            informacion[0] = contador;
            informacion[1] = persona.getCedula();
            informacion[2] = persona.getNombres();
            informacion[3] = persona.getApellidos();
            informacion[4] = persona.getEstado();
            informacion[5] = persona.getTipo();
            contador++;

            modelo.addRow(informacion);
        }
        tblDatos.setModel(modelo);

    }


    private Queue<Integer> sumarColas(Queue<Integer> cola1, Queue<Integer> cola2) {
        Queue<Integer> resultado = new LinkedList<>();

        // Verificar que ambas colas tengan la misma longitud
        if (cola1.size() != cola2.size()) {
            throw new IllegalArgumentException("Las colas deben tener la misma longitud");
        }

        // Sumar los elementos uno a uno y agregar el resultado a la nueva cola
        while (!cola1.isEmpty() && !cola2.isEmpty()) {
            int suma = cola1.poll() + cola2.poll();
            resultado.offer(suma);
        }

        return resultado;
    }


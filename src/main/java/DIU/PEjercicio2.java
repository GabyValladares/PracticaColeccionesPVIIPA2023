    Stack<Integer> torreInicio = new Stack<>();
    Stack<Integer> torreMedio = new Stack<>();
    Stack<Integer> torreDestino = new Stack<>();
    int contador = 0;

    private void btnResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResolverActionPerformed
        contador++;
        switch (contador) {
            case 1:
                discos(torreInicio, torreDestino);
                break;
            case 2:
                discos(torreInicio, torreMedio);
                break;
            case 3:
                discos(torreDestino, torreMedio);
                break;
            case 4:
                discos(torreInicio, torreDestino);
                break;
            case 5:
                discos(torreMedio, torreInicio);
                break;
            case 6:
                discos(torreMedio, torreDestino);
                break;
            case 7:
                discos(torreInicio, torreDestino);
                btnResolver.setVisible(false);
                btnReiniciar.setVisible(true);
                break;
        }
        
        setFila(modeloInicio, torreInicio);
        setFila(modeloMedio, torreMedio);
        setFila(modeloDestino, torreDestino);
    }//GEN-LAST:event_btnResolverActionPerformed

    private void formInternalFrameActivated(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameActivated
        btnReiniciar.setVisible(false);
        contador=0;
        inicio();

    }//GEN-LAST:event_formInternalFrameActivated

    private void btnReiniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReiniciarActionPerformed
        btnResolver.setVisible(true);
        btnReiniciar.setVisible(false);
        torreInicio.clear();
        torreMedio.clear();
        torreDestino.clear();
        inicio();
    }//GEN-LAST:event_btnReiniciarActionPerformed

    public void inicio() {
        contador=0;
        modeloDestino.setRowCount(0);
        modeloMedio.setRowCount(0);
        modeloInicio.setRowCount(0);
        for (int i = 3; i > 0; i--) {
            torreInicio.push(i);
        }
        setFila(modeloInicio, torreInicio);
    }

    public void setFila(DefaultTableModel modelo, Stack<Integer> torre) {
        modelo.setRowCount(0);
        for (int i = torre.size() - 1; i >= 0; i--) {
            modelo.addRow(new Object[]{torre.get(i)});
        }
    }

    public void discos(Stack<Integer> inicio, Stack<Integer> destino) {
        Integer numero = inicio.pop();
        destino.push(numero);
    }


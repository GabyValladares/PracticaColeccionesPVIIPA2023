    private void asignarNumeroAtencion(){
        int numero = numeroAtencion ++;
        String tipoServicio = JOptionPane.showInputDialog("Ingrese el tipo de servicio (Consultas, Cancelaciones, Solicitud): ").toUpperCase();
        switch (tipoServicio) {
            case "Consulta":
                colaConsultas.offer(numero);
                break;
            case "Cancelaciones":
                colaCancelaciones.offer(numero);
                break;
            case "Solicitud":
                colaSolicitudes.offer(numero);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Tipo de servicio no válido.");
        }
        
    }
    
    private void atenderCliente(){
        try{
            int numeroAtendido = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el número de antencion a atender: "));
            usuariosAtendidos.add(numeroAtendido);
        }catch (NumberFormatException e){
            JOptionPane.showInputDialog(this, "Número de atención no valido ");
      }
    }
    
    private void actualizarResultados(){
        txtResultado.setText("Cola Consultas: " + colaConsultas.toString() +
                             "\nCola Cancelaciones: " + colaCancelaciones.toString() +
                             "\nCola Solicitudes: " + colaSolicitudes.toString() +
                             "\nUsuarios Atendidos: " + usuariosAtendidos.toString());
    }

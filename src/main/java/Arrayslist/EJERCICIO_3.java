    private void btninicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninicioActionPerformed
       // TODO add your handling code here:
        int suma = 0;
        for (int n : numeros) {
            suma += n;
        }
        double promedio;
        if (numeros.isEmpty()) {
            promedio = 0;
        } else {
            promedio = (double) suma / numeros.size();
        }
        txtn2.setText(String.valueOf(suma));
        txtn3.setText(String.valueOf(promedio));
        ArrayList<Integer> mayoresAlPromedio = new ArrayList<>();
        for (int n : numeros) {
            if (n > promedio) {
                mayoresAlPromedio.add(n);
            }
        }
        txtn4.setText(String.valueOf(mayoresAlPromedio.size()));
        txtn4.setText(mayoresAlPromedio.toString());
    }//GEN-LAST:event_btninicioActionPerformed


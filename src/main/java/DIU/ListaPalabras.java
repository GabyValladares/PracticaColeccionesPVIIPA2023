    private void btniniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btniniciarActionPerformed
        // TODO add your handling code here:
          String oracion = txtoracion.getText();
        String palabra = txtpalabra.getText();

        // Divide la oración en palabras y agrega a la lista
        String[] palabras = oracion.split("\\s+");
        for (String palabraOracion : palabras) {
            listaPalabras.add(palabraOracion);
        }
        // Contar cuántas veces aparece la palabra en la lista
        int contador = contarPalabra(listaPalabras, palabra);
        txtveces.setText(String.valueOf(contador));

    }//GEN-LAST:event_btniniciarActionPerformed
private static int contarPalabra(ArrayList<String> lista, String palabra) {
        int contador = 0;
        for (String palabraLista : lista) {
            if (palabraLista.equals(palabra)) {
                contador++;
            }
        }
        return contador;
    }

